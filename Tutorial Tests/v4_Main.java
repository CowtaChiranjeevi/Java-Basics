public class v4_Main {
    public static void main(String[] args) {
        
        //counter++ (is the same as) counter = counter +1
        //Hereunder is a for loop 
        for(int counter1 = 0; counter1 <11; counter1++) {
            System.out.print(counter1+" ");
        }

        //Hereunder is a while loop
        int gh = 0;
        while(gh < 11) {
            System.out.print(gh+" "); gh = gh + 1;          
        }

        //Hereunder is a do while loop
        int hb1 = 5;
        do {
            System.out.println("Loopy Loops"); hb1 = hb1 +1;
        } while (hb1 < 10 ); 
        

    }
}
